export class Friend {
  firstName: string;
  lastName: string;
  email: string;
  number: string;
  language: string;

  constructor(
  firstName: string,
  lastName: string,
  email: string,
  number: string,
  language: string,
  ) {
   this.firstName = firstName;
   this.lastName = lastName;
   this.email = email;
   this.number = number;
   this.language = language;
  }
}
